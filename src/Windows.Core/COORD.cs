namespace PInvoke
{
    using System.Runtime.InteropServices;

    /// <summary>
    /// The COORD structure defines the X- and Y- coordinates of a point.
    /// </summary>
    public struct COORD
    {
        /// <summary>
        ///  The x-coordinate of the point.
        /// </summary>
        public short X;

        /// <summary>
        /// The x-coordinate of the point.
        /// </summary>
        public short Y;
    }
}
